    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(jtCat.getSelectedRow() != -1){
            try {
                Categoria ct = new Categoria();
                CategoriaDAO cdao = new CategoriaDAO();
                
                ct.setNome_cat(txtNCat.getText().toUpperCase());
                ct.setId_categoria((int) jtCat.getValueAt(jtCat.getSelectedRow(), 0));
                
                cdao.Atualiza(ct);
                
                DefaultTableModel dtmCat = (DefaultTableModel) jtCat.getModel();
                Object[] dadoscat = {txtNCat.getText().toUpperCase()};
                dtmCat.addRow(dadoscat);
                
                txtNCat.setText("");
                cbCat.setEnabled(true);
                combopop();
                consJt();
            } catch (SQLException ex) {
                Logger.getLogger(JfCategoria.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma linha");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jtCatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtCatMouseClicked
        if(jtCat.getSelectedRow() != -1){
            txtNCat.setText(jtCat.getValueAt(jtCat.getSelectedRow(), 1).toString());
            txtNCat.setEnabled(true);
            txtNCat.grabFocus();
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma linha");
        }
    }//GEN-LAST:event_jtCatMouseClicked

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(txtNCat.equals("")){
            
        }else{
            
            
            if(jtCat.getSelectedRow() != -1){
                try {
                    Categoria ct = new Categoria();
                    CategoriaDAO cdao = new CategoriaDAO();

                    ct.setId_categoria((int) jtCat.getValueAt(jtCat.getSelectedRow(), 0));

                    cdao.Deleta(ct);

                    DefaultTableModel dtmCat = (DefaultTableModel) jtCat.getModel();
                    dtmCat.removeRow(jtCat.getSelectedRow());

                    txtNCat.setText("");

                    combopop();
                    consJt();
                } catch (SQLException ex) {
                    Logger.getLogger(JfCategoria.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

