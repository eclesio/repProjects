    public void consJt() throws SQLException{
        DefaultTableModel modelTable = (DefaultTableModel) jtCat.getModel();
        modelTable.setNumRows(0);
        CategoriaDAO catdao = new CategoriaDAO();
        
        for(Categoria ct : catdao.Consultar()){
            modelTable.addRow(new Object[]{
                ct.getId_categoria(),
                ct.getNome_cat()
            });
        }
    }
    
    public void combopop(){
        CategoriaDAO catdao = new CategoriaDAO();
        cbCat.removeAllItems();
        cbCat.addItem("");
        
        try {
            for(Categoria ct: catdao.Consultar()){
                
                cbCat.addItem(ct.getNome_cat());
            }
        } catch (SQLException ex) {
            Logger.getLogger(JfCategoria.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

